package com.acgcompass.domain.taste

/**
 * 短评语义关键词抽取（最终版算法「评论语义」维度的轻量实现）。
 *
 * 用一张**精选评价词典**做子串命中，把用户短评（如「设定有想法，慢热但后劲足」）抽成稳定的语义标签
 * （`设定`/`慢热`/`后劲`...）并入 [TasteCategory.COMMENT]。只取高判别力的评价倾向词，避免无脑分词引入噪声；
 * 命中即记一次（不计频次），由样本权重 `w_i·pref_i` 决定方向与强度。纯函数、无 IO，便于单测。
 */
object CommentKeywords {

    /**
     * 评价倾向词典（清洗为小写后做子串匹配）。涵盖：节奏（慢热/拖沓/紧凑）、回味（后劲/上头/致郁/治愈）、
     * 制作（作画/演出/分镜/配乐/声优）、剧本（设定/伏笔/烧脑/反转/世界观/人物塑造）、情绪（催泪/感动/燃/中二）、
     * 负向（烂尾/崩坏/无聊/劝退/水/尴尬/说教/降智）。
     */
    val KEYWORDS: Set<String> = setOf(
        // 节奏
        "慢热", "拖沓", "紧凑", "节奏快", "节奏慢", "平淡", "高潮",
        // 回味 / 后劲
        "后劲", "上头", "致郁", "治愈", "回味", "意难平", "压抑", "温暖", "致郁系",
        // 制作
        "作画", "演出", "分镜", "配乐", "音乐", "声优", "画面", "经费", "ed", "op",
        // 剧本 / 设定
        "设定", "伏笔", "烧脑", "反转", "世界观", "剧情", "人物塑造", "群像", "脑洞", "叙事",
        "逻辑", "节奏感", "细节",
        // 情绪
        "催泪", "感动", "燃", "中二", "热血", "羁绊", "成长", "青春", "甜", "虐",
        // 负向
        "烂尾", "崩坏", "无聊", "劝退", "注水", "尴尬", "说教", "降智", "套路", "强行", "拉胯",
        "弃番", "出戏",
    ).map { it.lowercase() }.toSet()

    /**
     * 从短评中抽取命中的语义关键词（清洗后小写）。空 / 空白短评返回空集合。
     * 子串命中：短评包含词典词即记入（去重）。
     */
    fun extract(comment: String?): Set<String> {
        val text = comment?.trim()?.lowercase().orEmpty()
        if (text.isEmpty()) return emptySet()
        val out = LinkedHashSet<String>()
        for (kw in KEYWORDS) {
            if (kw.isNotEmpty() && text.contains(kw)) out += kw
        }
        return out
    }
}
